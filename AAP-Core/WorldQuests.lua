function AAP.WQFunc()
	AAP.WQActive = 0
	local i = 1
	while GetQuestLogTitle(i) do
		local questTitle, level, suggestedGroup, isHeader, isCollapsed, isComplete, frequency, questID = GetQuestLogTitle(i)
		if (questID > 0) then
			if (AAP.HordeWQList[questID]) then
				AAP.QuestStepList[questID] = AAP.HordeWQList[questID]
				AAP.WQActive = 1
				AAP.ActiveMap = questID
			end
		end
		i = i + 1
	end
end
